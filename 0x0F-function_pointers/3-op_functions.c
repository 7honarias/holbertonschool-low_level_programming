#include "3-calc.h"

/**
 * op_add - function for +
 * @a: firts number
 * @b: second number
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function for -
 * @a: firts number
 * @b: second number
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function for *
 * @a: firts number
 * @b: second number
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function for /
 * @a: firts number
 * @b: second number
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function for %
 * @a: firts number
 * @b: second number
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
